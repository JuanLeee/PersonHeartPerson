create table interests(
    iname char(20) primary key);

grant select on interests to public;

create table personalityPreference(
    pcode char(4) primary key);

grant select on personalityPreference to public;

create table clusterAccount(
    cid integer primary key,
    city char(10),
    province char(2),
    pcode char(4));

grant select on clusterAccount to public;

create table account(
    username char(20) primary key,
    upassword char(20),
    gender char(1),
    genderPreference char(1),
    aname char(20),
    email char(30) unique,
    city char(20) NOT NULL,
    province char(2) NOT NULL,
    phoneno char(10),
    personality char(4),
    cid integer NOT NULL,
    foreign key (cid) references clusterAccount ON DELETE SET NULL);

grant select on account to public;

create table pictureUpload(
    username char(20) NOT NULL,
    phoID integer,
    pdate date,
    primary key(username, phoID),
    foreign key (username) references account ON DELETE CASCADE);

grant select on pictureUpload to public;


create table checkOff(
    pcode char(4),
    username char(20),
    primary key (pcode, username),
    foreign key (pcode) references personalityPreference ON DELETE CASCADE,
    foreign key (username) references account ON DELETE CASCADE);

grant select on checkOff to public;

create table has(
    username char(20),
    iname char(20),
    primary key (username, iname),
    foreign key (username) references account ON DELETE CASCADE,
    foreign key(iname) references interests ON DELETE CASCADE);

grant select on has to public;







